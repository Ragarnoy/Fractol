#include "../include/fractol.h"

void	palette(t_env *env)
{
	int i;

	i = 0;
	env->pal[0].h = 0;
	env->pal[0].s = 1.0;
	env->pal[0].l = 1.0;
	while (++i < 37)
	{
		env->pal[i].h = i * 10;
		env->pal[i].s = 1.0;
		env->pal[i].l = 0.5;
	}
}
